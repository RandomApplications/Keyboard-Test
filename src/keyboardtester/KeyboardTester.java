    private void onKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_onKeyPressed
        int keycode = evt.getKeyCode();
        
        if(evt.isControlDown()) {
            switch(keycode) {
                // Clear the keys highlighted with Ctrl+C
                case java.awt.event.KeyEvent.VK_C:
                    privLabels.iterator().forEachRemaining((javax.swing.JLabel label) -> {
                        label.setBackground(new java.awt.Color(238, 238, 238));
                        label.setOpaque(false);
                    });
                    privLabels.removeAll(privLabels);
                    return;
                // Exit with Ctrl+X
                case java.awt.event.KeyEvent.VK_X:
                    System.exit(0);
            }
        }
        
        keytext.setText(java.awt.event.KeyEvent.getKeyText(keycode));
        try {
            String p = "";
            switch(evt.getKeyLocation()) {
                case java.awt.event.KeyEvent.KEY_LOCATION_LEFT: p = "l";
                break;
                case java.awt.event.KeyEvent.KEY_LOCATION_RIGHT: p = "r";
                break;
                case java.awt.event.KeyEvent.KEY_LOCATION_NUMPAD: p = "n";
            }
            
            // Fix for faliure in idenfying the location of the windows key
            if(java.awt.event.KeyEvent.getKeyText(keycode).toLowerCase().contains("windows") && 
                    evt.getKeyLocation()==java.awt.event.KeyEvent.KEY_LOCATION_STANDARD) {
                keycode = java.awt.event.KeyEvent.VK_META;
                javax.swing.JLabel ll = (javax.swing.JLabel)this.getClass()
                        .getDeclaredField("k"+Integer.toString(keycode)+"l_label").get(this);
                javax.swing.JLabel lr = (javax.swing.JLabel)this.getClass()
                        .getDeclaredField("k"+Integer.toString(keycode)+"r_label").get(this);
                
                ll.setBackground(new java.awt.Color(0, 170, 0));
                ll.setOpaque(true);
                lr.setBackground(new java.awt.Color(0, 170, 0));
                lr.setOpaque(true);
                privLabels.add(ll);
                privLabels.add(lr);
            } else {
                javax.swing.JLabel label = (javax.swing.JLabel)this.getClass()
                    .getDeclaredField("k"+Integer.toString(keycode)+p+"_label").get(this);
                label.setBackground(new java.awt.Color(0, 170, 0));
                label.setOpaque(true);
                privLabels.add(label);
            }
            
        } catch (NoSuchFieldException | SecurityException | IllegalArgumentException 
                | IllegalAccessException ex) {
            // No need to do anything for exceptions as they'll occur quite often
        }
    }//GEN-LAST:event_onKeyPressed

